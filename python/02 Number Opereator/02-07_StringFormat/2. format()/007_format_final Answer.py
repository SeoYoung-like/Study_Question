
num = 100000000
float_number = 15845783475.14159274572


# 천 단위 콤마 - 100,000,000 출력하기
print(format(num, ','))
print("{:,}".format(num))

# {} 문자 표현하기 - {100,000,000} 출력하기
print("{{{:,}}}".format(num))

# "=========+15,845,783,475.14159=========="로 출력되게 만드시오. (문자 길이: 40)
print("{:=^+40,.5f}".format(float_number))
