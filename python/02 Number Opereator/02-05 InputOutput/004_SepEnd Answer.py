# sep과 end를 사용해서 아래와 같이 출력 되게 만드시오.
# ==================================================================
# | k:0 | k:1 | k:2 | k:3 | k:4 | k:5 | k:6 | k:7 | k:8 | k:9 | 
# | k:1 | k:2 | k:3 | k:4 |
# | k:3 | k:6 | k:9 | k:12 |
# ==================================================================

li_big = [list(range(10)), list(range(1, 5)), list(range(3, 15, 3))]

for li_small in li_big:
    print("| ", end="")
    for i in li_small:
        print("k", i, sep=':', end=" | ")
    print()